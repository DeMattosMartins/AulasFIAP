def aprovado(i, rM, vE, nP):
    if i < 18 or vE > rM*15 or vE/nP < 100 or vE/nP > 2000:
        return False
    else:
        return True

def juros(vE, nP):
    if nP <= 6:
        return 0.05
    elif nP <= 12:
        return 0.08
    else:
        return 0.10

def parcelaFinal(vE, nP):
    return vE/nP

