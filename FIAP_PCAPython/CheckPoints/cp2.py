#funções do exercicio:
def precoCarga(codigoCarga):
    if codigoCarga <= 20:
      return  100
    elif codigoCarga <= 30:
      return 250
    else:
      return 340

def imposto(estado):
    match(estado):
        case 1:
          return 35/100
        case 2:
          return 25/100
        case 3:
            return 15/100
        case 4:
            return 5/100
        case _:
            return 0

#solicita e verifica o codigo do estado de origem
estado = int(input("Diga o código do estado: "))

#solicita o peso
peso = float(input("Peso da carga(em toneladas): "))

#solicita e verifica o codigo da carga
codigoCarga = int(input("Diga o codigo da carga: "))

#transforma toneladas em kilos
kilos = peso*1000
print(f"Peso total do caminhão em kilos: {kilos:.2f}.")
print()

#verifica o preço da carga do caminhão
preco = kilos*precoCarga(codigoCarga)
print(f"Preço total da carga: {preco:.2f}")
print()

#verifica o valor do imposto
valorImposto = imposto(estado)
print("Valor do imposto: ", valorImposto*100,"%")
print()

#Valor total da carga
valorTotal = preco + preco*valorImposto
print(f"Valor total da carga: {valorTotal:.2f}")
print()

